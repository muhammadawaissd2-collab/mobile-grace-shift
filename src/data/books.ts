/** Find books relevant to a search query */
export function searchBooks(query: string) {
  const q = query.toLowerCase();
  return books.filter(b =>
    b.title.toLowerCase().includes(q) ||
    b.author.toLowerCase().includes(q) ||
    b.description.toLowerCase().includes(q) ||
    b.tags.some(t => t.toLowerCase().includes(q)) ||
    b.chapters.some(ch =>
      ch.title.toLowerCase().includes(q) ||
      ch.topics?.some(t => t.toLowerCase().includes(q))
    )
  );
}

/** Find books related to a given topic (muscle name, impairment, exercise keyword) */
export function findRelatedBooks(keyword: string): { book: Book; matchedTopics: string[] }[] {
  const kw = keyword.toLowerCase();
  const results: { book: Book; matchedTopics: string[] }[] = [];

  for (const book of books) {
    const matchedTopics: string[] = [];
    // Check tags
    if (book.tags.some(t => t.toLowerCase().includes(kw) || kw.includes(t.toLowerCase()))) {
      matchedTopics.push("tag match");
    }
    // Check chapter topics
    for (const ch of book.chapters) {
      for (const topic of ch.topics || []) {
        if (topic.toLowerCase().includes(kw) || kw.includes(topic.toLowerCase())) {
          matchedTopics.push(`${ch.title}: ${topic}`);
        }
      }
    }
    if (matchedTopics.length > 0) {
      results.push({ book, matchedTopics });
    }
  }
  return results.slice(0, 5);
}
